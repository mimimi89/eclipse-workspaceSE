package com.itwill01_member_method;

public class Calculator {	//calculator class 안에 4개의 인스턴스 메쏘드
	
	public int add(int a, int b) {	
		return a+b;
		
	}
	
	public int sub(int a, int b) {
		return a-b;
	}
	
	public int mul(int a, int b) {
		return a*b;
	}
	
	public int div(int a, int b) {		//int(정수값)를 반환하는 사칙연산 메쏘드
		return a/b;
	}
	
	
	
	
	
}
