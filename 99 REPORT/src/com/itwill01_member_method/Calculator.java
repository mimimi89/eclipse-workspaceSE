package com.itwill01_member_method;

public class Calculator {

		
	 /* 4개의 멤버메쏘드
	 add( 두개의 정수를 인수로 받아서 더한결과를 반환)
	 subtract
	 div
	 multiple
	 */
	
	 	public int add(int a, int b) {
			return a+b;
			
		}
		
		public int sub(int a, int b) {
			return a-b;
			
		}
		
		public int mul(int a, int b) {
			return a*b;
			
		}
		
		public int div(int a, int b) {
			return a/b;	
			
		}
	
	
}
