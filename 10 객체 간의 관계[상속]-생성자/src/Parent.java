
public class Parent extends Object{
	
	public int member1;
	public int member2;
	
	
	
	public Parent() {
		super();
		System.out.println("Paren() 기본(디폴트) 생성자");
	}
	
	
	
	public void method1() {
		System.out.println("Parent.method1()");
	}
	public void method2() {
		System.out.println("Parent.method2()");
	}
	
	

}
